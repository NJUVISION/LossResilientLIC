import yaml
import torch
import shutil

class AverageMeter:
    """Compute running average."""

    def __init__(self):
        self.val = 0
        self.avg = 0
        self.sum = 0
        self.count = 0

    def update(self, val, n=1):
        self.val = val
        self.sum += val * n
        self.count += n
        self.avg = self.sum / self.count

def save_checkpoint(state, is_best, filename="checkpoint.pth.tar", bestname="checkpoint_best_loss.pth.tar"):
    torch.save(state, filename)
    if is_best:
        shutil.copyfile(filename, bestname)

